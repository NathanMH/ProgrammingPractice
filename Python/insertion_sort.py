def sort_array(array):
    for i in range(1, len(array)):
        temp = array[i]
        index = i
        while index > 0 and array[index - 1] > temp:
            array[index] = array[index - 1]
            index = index - 1
        array[index] = temp
    return array

###################################################################
# 3. Main
###################################################################

def sort(array):
    return sort_array(array)

